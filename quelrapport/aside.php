         		    <p> <strong>Quel Rapport?</strong></p>
  			  <p> 
            <em>"Liens entre"</em> en latin <em>"inter ligere"</em> est à l'origine du mot <strong>intelligence</strong>. Quel Rapport permet de faire des liens entre des concepts ou mots a priori éloignés, afin peut-être de mettre du sens dans le chaos du monde par l'emotions, la culture, la science mais aussi du vécu ou des anecdotes. Ou tout simplement s'étonner.
			     Pas de jeux de mots ou de blagues mais seulement des liens forts entre des mondes apparemment étanches.
			     Mieux encore que la poesie, les connections cachées du monde réel !
  		    </p>
